def _apply_castle_scene(scene_id: str):
    apply = api_post("/sync/apply", {
        "scene_id": scene_id,
        "mode": "apply_safe",
        "allow_delete": False,
        "max_operations": 200,
    })
    return assert_success(apply, "apply castle")


def _missing_castle_mcp_ids(objects):
    level_result = unreal_command("get_actors_in_level", {})
    actors = level_result.get("result", {}).get("actors", [])
    actual_mcp_ids = {
        tag.removeprefix("mcp_id:")
        for actor in actors
        for tag in actor.get("tags", [])
        if tag.startswith("mcp_id:")
    }
    return [
        obj["mcp_id"]
        for obj in objects
        if obj["mcp_id"] not in actual_mcp_ids
    ]
