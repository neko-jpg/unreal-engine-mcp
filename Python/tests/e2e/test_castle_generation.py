"""E2E test: generate a medieval European castle from scratch for siege warfare.

Requires:
    - SurrealDB on ws://127.0.0.1:8000
    - scene-syncd on http://127.0.0.1:8787
    - Unreal Editor with MCP Bridge on 127.0.0.1:55557

Run:
    pytest tests/e2e/test_castle_generation.py --skip-unreal   # DB + planner only
    pytest tests/e2e/test_castle_generation.py                  # Full E2E with Unreal
"""

import json
import time
import pytest

from .conftest import api_post, api_get, unreal_command, assert_success, SCENE_SYNCD_URL


class TestCastleGenerationDBOnly:
    """Tests requiring only scene-syncd (no Unreal)."""

    def test_castle_objects_bulk_upsert_and_plan(self, scene_syncd_available):
        """Create a medieval castle scene in DB and verify the sync plan shows CREATEs."""
        if not scene_syncd_available:
            pytest.skip("scene-syncd not available")

        suffix = time.strftime("%Y%m%d%H%M%S")
        scene_id = f"castle_db_{suffix}"

        api_post("/scenes/create", {
            "scene_id": scene_id,
            "name": f"Medieval Castle DB Test {suffix}",
            "description": "Siege warfare castle (DB-only test)",
        })

        try:
            objects = _build_castle_objects(scene_id, suffix)
            assert_success(
                api_post("/objects/bulk-upsert", {"scene_id": scene_id, "objects": objects}),
                "bulk upsert castle objects",
            )

            plan = api_post("/sync/plan", {"scene_id": scene_id})
            data = assert_success(plan, "plan castle")
            creates = [op for op in data.get("operations", []) if op.get("action") == "create"]
            assert len(creates) >= len(objects), \
                f"Expected >= {len(objects)} CREATEs, got {len(creates)}"
        finally:
            # Best-effort cleanup
            try:
                obj_list = api_post("/objects/list", {"scene_id": scene_id}).get("data", {}).get("objects", [])
                for obj in obj_list:
                    try:
                        api_post("/objects/delete", {"scene_id": scene_id, "mcp_id": obj.get("mcp_id", "")})
                    except Exception:
                        pass
            except Exception:
                pass


@pytest.mark.requires_unreal
class TestCastleGenerationWithUnreal:
    """Tests requiring a running Unreal Editor session."""

    def test_generate_medieval_castle(self, scene_syncd_available, unreal_available):
        """Build a medieval European castle in Unreal from scratch via scene-syncd + direct commands."""
        if not scene_syncd_available:
            pytest.skip("scene-syncd not available")
        if not unreal_available:
            pytest.skip("Unreal not available")

        suffix = time.strftime("%Y%m%d%H%M%S")
        scene_id = f"castle_{suffix}"

        # 1. Create scene
        api_post("/scenes/create", {
            "scene_id": scene_id,
            "name": f"Medieval Castle {suffix}",
            "description": "Siege warfare castle generated by E2E test",
        })

        try:
            # 2. Build castle objects in DB
            objects = _build_castle_objects(scene_id, suffix)
            assert_success(
                api_post("/objects/bulk-upsert", {"scene_id": scene_id, "objects": objects}),
                "bulk upsert castle objects",
            )

            # 3. Plan should show all CREATEs
            plan = api_post("/sync/plan", {"scene_id": scene_id})
            plan_data = assert_success(plan, "plan castle")
            creates = [op for op in plan_data.get("operations", []) if op.get("action") == "create"]
            assert len(creates) >= len(objects), \
                f"Expected >= {len(objects)} CREATEs, got {len(creates)}"

            # 4. Apply to Unreal
            apply_data = _apply_castle_scene(scene_id)
            summary = apply_data["summary"]
            assert summary["creates"] >= len(objects), \
                f"Expected >= {len(objects)} creates, got {json.dumps(summary, indent=2)}"

            # 5. Verify each castle actor exists in Unreal.
            # Query once rather than issuing one socket command per actor; the
            # bridge can abort bursts of short-lived TCP verification calls.
            missing_mcp_ids = _missing_castle_mcp_ids(objects)
            if missing_mcp_ids:
                # The editor bridge can report a create before the actor is
                # visible in the next level snapshot. Re-plan/apply once so
                # scene-syncd converges missing desired objects.
                _apply_castle_scene(scene_id)
                for _ in range(5):
                    missing_mcp_ids = _missing_castle_mcp_ids(objects)
                    if not missing_mcp_ids:
                        break
                    time.sleep(0.5)
            assert not missing_mcp_ids, \
                f"Castle actors missing in Unreal: {missing_mcp_ids}"

            # 6. Create NavMesh for the inner courtyard
            nav_result = unreal_command("create_nav_mesh_volume", {
                "volume_name": f"CastleNavMesh_{suffix}",
                "location": [0.0, 0.0, 0.0],
                "extent": [4500.0, 4500.0, 500.0],
            })
            nav_inner = nav_result.get("result", {})
            assert nav_inner.get("success") is not False, \
                f"NavMesh creation failed: {nav_result}"
            assert nav_inner.get("navmesh_rebuilt") is True, \
                f"NavMesh not rebuilt: {nav_result}"

            # 7. Create patrol route along the top of the walls
            patrol_result = unreal_command("create_patrol_route", {
                "patrol_route_name": f"CastleWallPatrol_{suffix}",
                "points": [
                    {"x": -4000.0, "y": -4000.0, "z": 800.0},
                    {"x": 4000.0, "y": -4000.0, "z": 800.0},
                    {"x": 4000.0, "y": 4000.0, "z": 800.0},
                    {"x": -4000.0, "y": 4000.0, "z": 800.0},
                ],
                "closed_loop": True,
            })
            patrol_inner = patrol_result.get("result", {})
            assert patrol_inner.get("success") is not False, \
                f"Patrol route creation failed: {patrol_result}"
            assert patrol_inner.get("point_count") == 4, \
                f"Patrol route point count mismatch: {patrol_result}"

            # 8. Set AI behavior for the north-west tower guard
            tower_nw_name = f"CastleTowerNW_{suffix}"
            ai_result = unreal_command("set_ai_behavior", {
                "actor_name": tower_nw_name,
                "faction": "hostile",
                "behavior_tree_path": "/Game/AI/BT_CastleGuard.BT_CastleGuard",
                "perception_radius": 2000.0,
            })
            # The behavior tree may not exist in the project; allow that error,
            # but verify the actor was found and tags were applied.
            ai_inner = ai_result.get("result", {})
            error = ai_inner.get("error", "")
            assert "Actor not found" not in str(error), \
                f"Tower guard actor not found for AI setup: {ai_result}"

        finally:
            # Cleanup: delete all objects from DB and apply to Unreal
            try:
                obj_list = api_post("/objects/list", {"scene_id": scene_id}).get("data", {}).get("objects", [])
                for obj in obj_list:
                    try:
                        api_post("/objects/delete", {"scene_id": scene_id, "mcp_id": obj.get("mcp_id", "")})
                    except Exception:
                        pass
                api_post("/sync/apply", {
                    "scene_id": scene_id,
                    "mode": "apply_safe",
                    "allow_delete": True,
                    "max_operations": 200,
                })
            except Exception:
                pass


